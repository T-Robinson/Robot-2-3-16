package org.usfirst.frc.team3597.robot;
/**
 * The RobotMap is a mapping from the ports sensors and actuators are wired into
 * to a variable name. This provides flexibility changing wiring, makes checking
 * the wiring easier and significantly reduces the number of magic numbers
 * floating around.
 */
public class RobotMap {
    public static int LeftTalon = 0;
    public static int RightTalon = 1; 
    public static int TestTalon = 69;
    public static int ShooterLoad = 2; 
    public static int ShooterFire = 3; 
    
    public static int ShooterSwitch = 0; 
    public static int TestSwitch = 69; 
    
}
